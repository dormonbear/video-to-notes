package prompt

// VideoNote 指示模型观看整段视频后输出一篇成稿文章（而非逐字稿）。
// 实际结构由 response_format 的 json_schema 强制，prompt 只描述任务与语言。
const VideoNote = `你是一个资深内容编辑。这是一段完整视频（包含画面与语音），请认真观看后用中文输出：
1. title：不超过 20 字、能概括主题的标题（用作博客标题，不含特殊符号）。
2. summary：一句话概括全文主旨（用作摘要/description）。
3. tags：3-6 个主题标签（不带 # 号，简短名词）。
4. article：一篇结构化、可独立阅读的中文文章正文（markdown 格式）。要求：
   - 这是二次创作整理，不是逐字稿——重新组织语言、提炼逻辑，让没看过视频的人也能读懂。
   - 充分利用画面信息：演示步骤、屏幕上的文字/代码/图表、关键操作都写进去。
   - 用 ## 二级标题分节，可用列表、代码块、引用等 markdown 元素。
   - 不要出现"视频里说""画面中"这类口播式表述，直接以文章口吻陈述。
   - 不要把 title 作为一级标题重复，正文直接从内容开始。
严格按要求的 JSON schema 输出，不要输出多余文字。`
